#Upper Case
a = "Hello,World!   "
print(a.upper())

#Lower Case
print(a.lower())

#Remove Whitespace
print(a.strip())

#Replace String
print(a.replace("H","j"))

#Split String
#The split() method returns a list where the text between the specified separator becomes the list items.

print(a.split(','))

